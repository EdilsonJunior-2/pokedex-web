export const typeName = (type) => {
    if (type === "grass") return "grama";
    if (type === "fire") return "fogo";
    if (type === "water") return "água";
    if (type === "normal") return "normal";
    if (type === "fighting") return "lutador";
    if (type === "flying") return "voador";
    if (type === "poison") return "venenoso";
    if (type === "ground") return "terra";
    if (type === "rock") return "pedra";
    if (type === "bug") return "inseto";
    if (type === "ghost") return "fantasma";
    if (type === "steel") return "metal";
    if (type === "electric") return "elétrico";
    if (type === "psychic") return "psíquico";
    if (type === "ice") return "gelo";
    if (type === "dragon") return "dragão";
    if (type === "dark") return "noturno";
    if (type === "fairy") return "fada";
} 